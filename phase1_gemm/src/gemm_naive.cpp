#include "gemm_naive.h"

// ===========================================================================
// 版本 1：naive ijk —— 最朴素的矩阵乘法，作为 baseline
// ===========================================================================
// 这个实现故意写得最直白，目的是：
//   1. 作为正确性参考（其他优化版本都要对齐它）
//   2. 作为性能基准（所有优化都要和它对比提升）
// 不要跳过 naive 直接优化，没有 baseline 你无法衡量优化效果。
// ===========================================================================
void gemm_naive_ijk(int M, int N, int K,
                    const std::vector<float>& A,
                    const std::vector<float>& B,
                    std::vector<float>& C) {
    // 第 1 步：初始化 C 为 0。
    // 为什么必须清零？后面用 += 累加，C 有垃圾值结果就错。
    // resize 在已是目标大小时不重新分配（比 clear()+resize 高效）。
    C.resize(M * N, 0.0f);

    // 第 2 步：三重循环 ijk。
    // 顺序含义：固定 C 的一个元素 C[i][j]，遍历 k 累加。
    for (int i = 0; i < M; ++i) {
        for (int j = 0; j < N; ++j) {
            // acc 放寄存器里（编译器会做），减少对 C[i*N+j] 的反复读写。
            // 如果直接写 C[i*N+j] += ..., 每次都要 load-C、加、store-C，慢。
            float acc = 0.0f;
            for (int k = 0; k < K; ++k) {
                // 核心一行，整个 Phase 1 的矛盾所在：
                //   A[i*K+k]：k 递增时连续访问 → cache 友好 ✅
                //   B[k*N+j]：k 递增时跨行，每次跳 4N 字节 → cache 不友好 ❌
                acc += A[i * K + k] * B[k * N + j];
            }
            // 一次写回，避免循环内反复写 C。
            C[i * N + j] = acc;
        }
    }
}

// ===========================================================================
// 版本 2：naive ikj —— 只调循环顺序，不写新算法
// ===========================================================================
// 为什么 ikj 比 ijk 快？
//   ijk: 内层 k，A 按行(好) B 按列(差)
//   ikj: 内层 j，A 不变(提到外层) B 按行(好) C 按行(好)
// 两个读源都连续，输出 C 虽反复写但可被寄存器/分块缓解。
// 这是"不写新算法、只调顺序"就能拿到的 2-3 倍提升，永远是第一步优化。
// ===========================================================================
void gemm_naive_ikj(int M, int N, int K,
                    const std::vector<float>& A,
                    const std::vector<float>& B,
                    std::vector<float>& C) {
    C.assign(M * N, 0.0f);  // assign 比 resize 更明确：清零 + 设置大小

    for (int i = 0; i < M; ++i) {
        for (int k = 0; k < K; ++k) {
            // 把 A[i*K+k] 提到内层循环外。
            // 作用：减少寻址计算，编译器更易把它放进寄存器。
            // 这就是"循环不变量外提（loop-invariant code motion）"，编译器优化基础。
            float a_ik = A[i * K + k];
            for (int j = 0; j < N; ++j) {
                // 现在内层循环：
                //   a_ik 在寄存器里不动
                //   B[k*N+j] 按行连续访问 → cache 友好 ✅
                //   C[i*N+j] 按行连续访问 → cache 友好 ✅
                C[i * N + j] += a_ik * B[k * N + j];
            }
        }
    }
}
